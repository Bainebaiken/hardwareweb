// Number Methods
// toString() Method
let x = 123;
 x.toString();
console.log(x.toString())

// toExponential()returns a string, with a number rounded and written using exponential notation.
 
let y = 9.656;
y.toExponential(2);
console.log(y.toExponential(2))

// toFixed() Method returns a string, with the number written with a specified number of decimals:
let z = 9.656;
z.toFixed(0);
console.log(z.toFixed(2))

// toPrecision() Method
let m = 9.656;
m.toFixed(2); 
console.log(m.toPrecision(2))

// valueOf() Method
let h = 123;
h.valueOf();
console.log(h.valueOf())


